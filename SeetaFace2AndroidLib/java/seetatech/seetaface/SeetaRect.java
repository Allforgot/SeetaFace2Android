package com.seetatech.seetaface;

public class SeetaRect {
    public int x;
    public int y;
    public int width;
    public int height;

    public SeetaRect(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}